#include<iostream>
using namespace std;

int main()
{
  int a=8, b=3;
  
  //Addition Opertor 
  cout<<"a+b= "<<(a+b)<<endl;
  
  //Subtraction Opertor
  cout<<"a-b= "<<(a-b)<<endl;
  
  //Multiplication Opertor
  cout<<"a*b= "<<(a*b)<<endl;
  
  //Division Opertor
  cout<<"a/b= "<<(a/b)<<endl;
  
  //Modulo Opertor
  cout<<"a%b= "<<(a%b)<<endl;
  
  return 0;
  
}